async function seedData() {
  const client = await pool.connect();
  
  try {
    await client.query('BEGIN');
    
    console.log('ðŸš€ Starting to seed faculties and curriculums...\n');
    
    // Insert faculties
    console.log('ðŸ“š Inserting faculties...');
    const facultyIds = {};
    
    for (const faculty of faculties) {
      // Check if faculty exists
      const existing = await client.query(
        'SELECT id FROM faculties WHERE name = $1',
        [faculty.name]
      );
      
      if (existing.rows.length > 0) {
        facultyIds[faculty.name] = existing.rows[0].id;
        console.log(`  âœ“ Faculty "${faculty.name}" already exists (ID: ${existing.rows[0].id})`);
      } else {
        const result = await client.query(
          'INSERT INTO faculties (name) VALUES ($1) RETURNING id',
          [faculty.name]
        );
        facultyIds[faculty.name] = result.rows[0].id;
        console.log(`  + Added faculty "${faculty.name}" (ID: ${result.rows[0].id})`);
      }
    }
    
    // Insert curriculums
    console.log('\nðŸ“– Inserting curriculums...');
    let addedCount = 0;
    let existingCount = 0;
    
    for (const [facultyName, curriculums] of Object.entries(curriculumsByFaculty)) {
      const facultyId = facultyIds[facultyName];
      
      if (!facultyId) {
        console.log(`  âš  Faculty "${facultyName}" not found, skipping curriculums`);
        continue;
      }
      
      console.log(`\n  Faculty: ${facultyName}`);
      
      for (const curriculum of curriculums) {
        // Check if curriculum exists
        const existing = await client.query(
          'SELECT id FROM curriculums WHERE name = $1 AND faculty_id = $2',
          [curriculum.name, facultyId]
        );
        
        if (existing.rows.length > 0) {
          existingCount++;
          console.log(`    âœ“ "${curriculum.name}" already exists`);
        } else {
          await client.query(
            'INSERT INTO curriculums (name, level, faculty_id) VALUES ($1, $2, $3)',
            [curriculum.name, curriculum.level, facultyId]
          );
          addedCount++;
          console.log(`    + Added "${curriculum.name}" (${curriculum.level})`);
        }
      }
    }
    
    await client.query('COMMIT');
    
    console.log('\nâœ… Seeding completed!');
    console.log(`   - Faculties: ${faculties.length}`);
    console.log(`   - Curriculums added: ${addedCount}`);
    console.log(`   - Curriculums already existed: ${existingCount}`);
    
  } catch (error) {
    await client.query('ROLLBACK');
    console.error('âŒ Error seeding data:', error);
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

